package com.arfajarsetiaji.android.popularmovies.model;

import android.os.Parcel;
import android.os.Parcelable;

/**
 * Created by Ar Fajar Setiaji on 29-Aug-17.
 */

/**
 * Struktur POJO MovieReview == JsonObject dari Movie Review.
 * Implements parcelable agar object movie dapat dikirim dengan bundle menggunakan intent.
 */

public class MovieReview implements Parcelable{

    private String id;
    private String author;
    private String content;
    private String url;

    public MovieReview() {
    }

    protected MovieReview(Parcel in) {
        id = in.readString();
        author = in.readString();
        content = in.readString();
        url = in.readString();
    }

    public static final Creator<MovieReview> CREATOR = new Creator<MovieReview>() {
        @Override
        public MovieReview createFromParcel(Parcel in) {
            return new MovieReview(in);
        }

        @Override
        public MovieReview[] newArray(int size) {
            return new MovieReview[size];
        }
    };

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel dest, int flags) {
        dest.writeString(id);
        dest.writeString(author);
        dest.writeString(content);
        dest.writeString(url);
    }
}
